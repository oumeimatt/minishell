#include "libft.h"

char	*ft_strmapi(char const *s, char (*f) (unsigned int, char))
{
	size_t	i;
	size_t	length;
	char	*result;

	i = 0;
	length = ft_strlen((char *)s);
	result = (char *)malloc(sizeof(char) * (length + 1));
	while (s[i] != '\0')
	{
		result[i] = f(i, s[i]);
		i++;
	}
	result[i] = '\0';
	return (result);
}
